<section>
  <h3>How to Avoid Red Flags</h3>
  <p>Protect yourself from scams and fraudulent activities by recognizing common red flags:</p>
  <ul>
    <li><strong>Unrealistic promises:</strong> Offers that sound too good to be true, like guaranteed high returns with no risk.</li>
    <li><strong>Pressure to act quickly:</strong> Urging you to make decisions without time to think or verify.</li>
    <li><strong>Requests for personal information:</strong> Asking for sensitive data like passwords, PINs, or bank details upfront.</li>
    <li><strong>Unsolicited contacts:</strong> Unexpected calls, emails, or messages from unknown parties.</li>
    <li><strong>Lack of transparency:</strong> No clear terms, contracts, or verifiable information.</li>
    <li><strong>Payment methods:</strong> Insisting on unusual payment methods like wire transfers, gift cards, or cryptocurrency without reason.</li>
  </ul>
  <p>Always verify information through official channels and report suspicious activities.</p>
</section>

